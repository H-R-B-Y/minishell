#include "vec3d.h"

t_vec3	vec3_cross(t_vec3 a, t_vec3 b)
{
	return ((t_vec3){
		.y = a.z * b.x - a.x * b.z,
		.z = a.x * b.y - a.y * b.x,
		.x = a.y * b.z - a.z * b.y
	});
}
