#include "vec3d.h"

t_vec3	vec3_add(t_vec3 vec1, t_vec3 vec2)
{
	return ((t_vec3){.x = vec1.x + vec2.x,
		.y = vec1.y + vec2.y,
		.z = vec1.z + vec2.z});
}
